public class Calculator {
// 1.Metody definiujące funkcje kalkulatora
    // Dodawanie, odejmowanie, mnożenie

    public int add (int x, int y) {
        return x + y;
    }
    public int sub (int x, int y) {
        return x-y;
    }
    public int multiply (int x, int y) {
        return x*y;
    }
}
